package org.ow2.play.metadata.api;

import java.util.ArrayList;
import java.util.List;

/**
 * A metadata has a name and a list of attached data (even if in general we will
 * have only one attached data).
 * 
 * @author chamerling
 * 
 */
public class Metadata {

	private String name;

	private List<Data> data;

	/**
	 * 
	 */
	public Metadata() {
		data = new ArrayList<Data>();
	}

	/**
	 * 
	 * @param name
	 * @param data
	 */
	public Metadata(String name, List<Data> data) {
		super();
		this.name = name;
		this.data = data;
	}

	public Metadata(String name, Data data) {
		this();
		this.name = name;
		this.data.add(data);
	}

	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}

	/**
	 * @param name
	 *            the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}

	/**
	 * @return the data
	 */
	public List<Data> getData() {
		return data;
	}

	/**
	 * @param data
	 *            the data to set
	 */
	public void setData(List<Data> data) {
		this.data = data;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "Metadata [name=" + name + ", data=" + data + "]";
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((data == null) ? 0 : data.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		Metadata other = (Metadata) obj;
		if (data == null) {
			if (other.data != null)
				return false;
		} else if (!data.equals(other.data))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}

}
