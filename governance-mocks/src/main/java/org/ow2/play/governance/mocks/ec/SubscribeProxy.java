package org.ow2.play.governance.mocks.ec;

import java.io.IOException;
import java.util.logging.Logger;

import javax.servlet.ServletException;
import javax.servlet.http.HttpServlet;
import javax.servlet.http.HttpServletRequest;
import javax.servlet.http.HttpServletResponse;

/**
 * @author chamerling
 * 
 */
public class SubscribeProxy extends HttpServlet {

	/**
	 * 
	 */
	private static final long serialVersionUID = 4536759415900529829L;

	private Logger logger = Logger.getLogger(SubscribeProxy.class.getName());

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * javax.servlet.http.HttpServlet#doPost(javax.servlet.http.HttpServletRequest
	 * , javax.servlet.http.HttpServletResponse)
	 */
	@Override
	protected void doPost(HttpServletRequest req, HttpServletResponse resp)
			throws ServletException, IOException {
		logger.info("GOT POST MESSAGE on SubscribeProxy");
		logger.info(req.getRequestURI());
		System.out.println(org.apache.commons.io.IOUtils.toString(req
				.getInputStream()));
		// TODO : print req
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see
	 * javax.servlet.http.HttpServlet#doGet(javax.servlet.http.HttpServletRequest
	 * , javax.servlet.http.HttpServletResponse)
	 */
	@Override
	protected void doGet(HttpServletRequest req, HttpServletResponse resp)
			throws ServletException, IOException {
		logger.info("GOT GET MESSAGE on SubscribeProxy");
		logger.info(req.getRequestURI());
		System.out.println(org.apache.commons.io.IOUtils.toString(req
				.getInputStream()));
	}

}
